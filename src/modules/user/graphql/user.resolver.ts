  @Mutation(() => UserResponse<User>)
  async createOrUpdateUser(
    @Args("data", { type: () => CreateOrUpdateUserDto })
    data: CreateOrUpdateUserDto
  ): Promise<UserResponse<User>> {
    if (data.id) {
      const existingUser = await this.service.findById(data.id);
      if (existingUser) {
        return this.commandBus.execute(
          new UpdateUserCommand(data, {
            instance: data,
            metadata: {
              initiatedBy:
                (data.input as CreateUserDto | UpdateUserDto).createdBy ||
                'system',
              correlationId: data.id,
            },
          })
        );
      }
    }
    return this.commandBus.execute(
      new CreateUserCommand(data, {
        instance: data,
        metadata: {
          initiatedBy:
            (data.input as CreateUserDto | UpdateUserDto).createdBy ||
            'system',
          correlationId: data.id || uuidv4(),
        },
      })
    );
  }


