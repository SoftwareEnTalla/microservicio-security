  @Mutation(() => LoginResponse<Login>)
  async createOrUpdateLogin(
    @Args("data", { type: () => CreateOrUpdateLoginDto })
    data: CreateOrUpdateLoginDto
  ): Promise<LoginResponse<Login>> {
    if (data.id) {
      const existingLogin = await this.service.findById(data.id);
      if (existingLogin) {
        return this.commandBus.execute(
          new UpdateLoginCommand(data, {
            instance: data,
            metadata: {
              initiatedBy:
                (data.input as CreateLoginDto | UpdateLoginDto).createdBy ||
                'system',
              correlationId: data.id,
            },
          })
        );
      }
    }
    return this.commandBus.execute(
      new CreateLoginCommand(data, {
        instance: data,
        metadata: {
          initiatedBy:
            (data.input as CreateLoginDto | UpdateLoginDto).createdBy ||
            'system',
          correlationId: data.id || uuidv4(),
        },
      })
    );
  }


